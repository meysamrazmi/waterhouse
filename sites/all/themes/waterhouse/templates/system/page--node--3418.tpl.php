      <h2>درباره جشنواره</h2>
      <p>جشنواره آثار خبری در حوزه تالاب برای اولین بار به شکل تخصصی در حوزه تالاب به همت طرح ملی حفاظت از تالابهای ایران طرح ریزی شده است.<br>
        خبرنگاران و علاقه‌مندان به حوزه محیط‌زیست میتوانند آثار خود را در سه بخش خبر، عکس و فیلم و موشن‌گرافیک مطابق با فرمت اعلامی ارسال کنند.<br> آثار ارسالی گنجینه‌ای ارزشمند برای جامعه محیط‌زیست کشور خواهد بود.

        به آثار منتخب جوایز ارزشمندی اهدا خواهد شد</p>
