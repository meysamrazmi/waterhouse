      <h2>فراخوان جشنواره آثار خبری در حوزه تالاب</h2>
      <p>مدیر ملی طرح حفاظت از تالاب های ایران ضمن تاکید بر اهمیت موضوع تالاب ها و نیاز روز افزون به گسترش آگاهی عمومی <br>
        و افزایش سواد رسانه‌ای شهروندان بخصوص فعالان حوزه آب و تالاب از برگزاری اولین جشنواره آثار خبری در حوزه تالاب خبر داد.</p>
      <br><br>

      <h2>این مسابقه در سه بخش برگزار می شود:</h2>
      <p>
        بخش اول: خبر و گزارش
        <br>
        بخش دوم: عکس و اینفوگرافیک
        <br>
        بخش سوم: فیلم و موشن گرافیک
      </p>
      <br><br>

      <div>
        <h2>مقررات عمومی شرکت در این رویداد:</h2>
      <p>
        • شرکت در مسابقه برای کلیه افراد آزاد است؛
        <br>
        •  آثار ارسالی در این مسابقه می‌بایست توسط خود شخص تهیه شده باشد؛
        <br>
        • هر فایل می بایست با نام تهیه کننده فایل و تاریخ تهیه و منطقه جغرافیایی اثر نام‌گذاری شود مثال (Ali Ahmadi-10 MEHR-URMIA ) : و باید به همراه آثار ارسالی فرم تعیین شده شامل مشخصات عکاس، نام و نام خانوادگی، شماره ملی،  شماره تماس همراه و ثابت، آدرس پستی و آدرس ایمیل ارسال شود؛
        <br>
        • شرکت کنندگان می باسیت نسخه اصلی اثر را در صورت نیاز به دبیرخانه ارسال نمایند.
        <br>
        • عکس های ارسالی در بخش حرفه ای می بایست در فرمت JPEG و دارای 2.5 مگابایت حجم باشند؛
        <br>
        • فیلم های ارسالی در بخش حرفه ای می بایست در فرمت MP4 و دارای حداکثر 10 مگابایت حجم و 3 دقیقه زمان باشند؛
        <br>
        • خبر و گزارشهای ارسالی در قالب فایل Word  و حداکثر 1000 کلمه باشند؛
        <br>
        • عکس و فیلمهای ارسالی نباید دارای امضاء عکاس، تاریخ، پاسپارتو، قاب، حاشیه، واترمارک و لوگو باشند؛
        <br>
        • آثار برگزیده در بخش فیلم و اینفوگرافیک در پایان جشنواره با کیفیت HD در اختیار دبیرخانه قرار گیرد؛
        <br>
        • شایسته است از ارسال آثاری که در دیگر جشنواره ها شرکت داده شده اند در این مسابقه خودداری فرمایید .
      </p>
